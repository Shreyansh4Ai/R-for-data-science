# Question 1: Write an R program to create a vector containing the numbers 5, 10, 15, 20, 25. Calculate the sum, mean, and standard deviation of the vector.

# Create vector
vec <- c(5, 10, 15, 20, 25)

# Calculate sum
sum_vec <- sum(vec)
print(paste("Sum:", sum_vec))

# Calculate mean
mean_vec <- mean(vec)
print(paste("Mean:", mean_vec))

# Calculate standard deviation
sd_vec <- sd(vec)
print(paste("Standard Deviation:", sd_vec))

