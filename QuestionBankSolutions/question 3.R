# Question 3: Write an R program to generate a sequence from 1 to 50 with an interval of 5, and calculate the square of each element in the sequence.

# Generate sequence from 1 to 50 with interval of 5
seq_vec <- seq(1, 50, by = 5)
print("Sequence:")
print(seq_vec)

# Calculate square of each element
square_vec <- seq_vec^2
print("Square of each element:")
print(square_vec)

